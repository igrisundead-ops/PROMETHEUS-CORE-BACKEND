export type MetadataFieldType =
  | "string"
  | "number"
  | "boolean"
  | "enum"
  | "string[]"
  | "object"
  | "number[]";

export type MetadataCatalogEntry = {
  key_path: string;
  group: string;
  label: string;
  type: MetadataFieldType;
  enum_values?: string[];
  required_when: string;
  default_behavior: string;
  help_text: string;
};

const buildEntries = (
  group: string,
  entries: Array<{
    key: string;
    label: string;
    type: MetadataFieldType;
    enum_values?: string[];
    required_when?: string;
    default_behavior: string;
    help_text: string;
  }>
): MetadataCatalogEntry[] => {
  return entries.map((entry) => ({
    key_path: `${group}.${entry.key}`,
    group,
    label: entry.label,
    type: entry.type,
    enum_values: entry.enum_values,
    required_when: entry.required_when ?? "optional",
    default_behavior: entry.default_behavior,
    help_text: entry.help_text
  }));
};

